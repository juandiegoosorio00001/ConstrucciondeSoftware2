package app.domain.models;

import java.sql.Date;

public class invoice {
	private long invoiceId;
    private long mascotId;
    private long ownerDocument;
    private long orderId;  
    private String productName;
    private double value;
    private int quantity;
    private Date date;
	public invoice(long invoiceId, long mascotId, long ownerDocument, long orderId, String productName, double value,
			int quantity, Date date) {
		super();
		this.invoiceId = invoiceId;
		this.mascotId = mascotId;
		this.ownerDocument = ownerDocument;
		this.orderId = orderId;
		this.productName = productName;
		this.value = value;
		this.quantity = quantity;
		this.date = date;
	}
	public long getInvoiceId() {
		return invoiceId;
	}
	public void setInvoiceId(long invoiceId) {
		this.invoiceId = invoiceId;
	}
	public long getMascotId() {
		return mascotId;
	}
	public void setMascotId(long mascotId) {
		this.mascotId = mascotId;
	}
	public long getOwnerDocument() {
		return ownerDocument;
	}
	public void setOwnerDocument(long ownerDocument) {
		this.ownerDocument = ownerDocument;
	}
	public long getOrderId() {
		return orderId;
	}
	public void setOrderId(long orderId) {
		this.orderId = orderId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
    
    
}
